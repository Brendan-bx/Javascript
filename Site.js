

function toggleImagetop1() {
    var img1 = "IMG/medal1.png";
    var img2 = "IMG/oui.png";
    var imgElement = document.getElementById('toggleImagetop1');
 
    imgElement.src = (imgElement.src === img1)? img2 : img1;

 }


