
function toggleImagetop1() {
    let img1 = "IMG/medal1.png";
    let img2 = "IMG/oui.png";
    let imgElement = document.getElementById('toggleImagetop1');
    
    imgElement.src = (imgElement.src === img1)? img2 : img1;

 }

